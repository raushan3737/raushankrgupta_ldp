/*
Variables: Java Variables are the data containers that save the data values during Java program execution.
Every variable is assigned a data type that designates the type and quantity of value it can hold.
 A variable is a memory location name for the data.
Float: 7 Precision , Double: 16 digits precision
 */
package com.javapoc.basics;

public class Variables {
    public static void main(String[] args) {
        // Variables
        int myNum = 5;
        long num = 15000000L;
        double myNumber = 19.99d;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String greeting = "Hello World";
        byte aByte = 127;
    }
}
